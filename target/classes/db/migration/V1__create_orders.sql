CREATE TABLE orders (
  id                  BIGSERIAL       NOT NULL,
  order_id            VARCHAR(50)     NOT NULL,
  label               VARCHAR(40)     NOT NULL,
  side                VARCHAR(3)      NOT NULL,
  sltp                VARCHAR(10)     NOT NULL,

  create_time         TIMESTAMP       NOT NULL,
  fill_time           TIMESTAMP       NOT NULL,
  open_price          NUMERIC(15, 14) NOT NULL,
  close_price         NUMERIC(15, 14) NOT NULL,
  close_time          TIMESTAMP       NOT NULL,
  duration            INT4            NOT NULL,
  ema50slope          NUMERIC(15, 14) NOT NULL,
  ema100slope         NUMERIC(15, 14) NOT NULL,
  ema_delta           NUMERIC(15, 14) NOT NULL,
  ema_delta_direction VARCHAR(25)     NOT NULL,
  ema_slope_strong    BOOLEAN         NOT NULL,
  ema_trend_correct   BOOLEAN         NOT NULL,
  stoch_slope_long    NUMERIC(15, 14) NOT NULL,
  stoch_slope_short   NUMERIC(15, 14) NOT NULL,
  last_ema_cross_over TIMESTAMP,

  reversals           INTEGER         NOT NULL,
  false_start         BOOLEAN         NOT NULL,
  stale               BOOLEAN         NOT NULL,
  stoch_strong        BOOLEAN         NOT NULL,
  strong_reversal     BOOLEAN         NOT NULL,
  strong_stoch_swing  BOOLEAN         NOT NULL,
  profit_loss         NUMERIC(5, 2)   NOT NULL,
  successful          BOOLEAN         NOT NULL,
  mitigation_applied  BOOLEAN         NOT NULL,
  PRIMARY KEY (id)
)